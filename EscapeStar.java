public class EscapeStar {
    public static void main(String[] args){

        //Making a right half pyramid in a single print statement line
        System.out.println("* \n** \n*** \n**** \n*****");

        //Making a reverse right half pyramid in a single print state,emt
        System.out.println("***** \n**** \n*** \n** \n*");

        //Making a left half pyramid in a single print statement
        System.out.println("     * \n    ** \n   *** \n  **** \n *****");
    }
}