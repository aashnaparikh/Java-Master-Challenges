package Challenges;

public class StarPatterns {
    public static void main(String[] args) {
        System.out.println("Right Star Pattern");

        System.out.println("*");
        System.out.println("**");
        System.out.println("***");
        System.out.println("****");
        System.out.println("*****");

        System.out.println("");
        System.out.println("");

        System.out.println("Reverse Star Pattern");

        System.out.println("*****");
        System.out.println("****");
        System.out.println("***");
        System.out.println("**");
        System.out.println("*");
        
        System.out.println("");
        System.out.println("");

        System.out.println("Left Star Pattern");

        System.out.println("     *");
        System.out.println("    **");
        System.out.println("   ***");
        System.out.println("  ****");
        System.out.println(" *****");
        
        
    }
}
